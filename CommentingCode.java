package App;

public class CommentingCode {
	public void commenting() {
		//this code added to explain code
		//all things after the two // are considered comments and not 
		//included when the code compiles
		System.out.println("Normal code is compiled and runs...");
		System.out.println("Comments however... ");
		//don't execute
		System.out.println("....");
		//Comments can be on their own line or they can share
		//lines like this
		/* these are multi line comments
		 * they keep going and allow you to 
		 * offer fellow devs necessary details
		 * about the reasoning for the code
		 */
		System.out.println("the end :) ");
	}
	
}